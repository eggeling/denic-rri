/* SQL commands to create a database for denic RRI*/


CREATE DATABASE denic CHARACTER SET utf8 COLLATE utf8_bin;
CREATE USER 'denic-rri'@'%';
SET PASSWORD FOR 'denic-rri'@'%' = PASSWORD('please-change');
GRANT ALL PRIVILEGES ON denic.* TO 'denic-rri'@'%';
FLUSH PRIVILEGES;

USE `denic`;

DROP TABLE IF EXISTS `country_code`;
CREATE TABLE `country_code` (
  `cc_id` int(255) NOT NULL auto_increment,
  `cc_code` varchar(255) NOT NULL,
  `cc_value` varchar(255) NOT NULL,
  `cc_description` varchar(255) NOT NULL,
  PRIMARY KEY  (`cc_id`)
);

DROP TABLE IF EXISTS `domain`;
CREATE TABLE `domain` (
  `dm_id` int(255) NOT NULL auto_increment,
  `dm_action` varchar(255) default NULL,
  `dm_domain` varchar(66) default NULL,
  `dm_domain_ace` varchar(66) default NULL,
  `dm_holder` varchar(32) default NULL,
  `dm_admin` varchar(32) default NULL,
  `dm_tech` varchar(32) default NULL,
  `dm_zone` varchar(32) default NULL,
  `dm_ns_0` varchar(254) default NULL,
  `dm_ns_1` varchar(254) default NULL,
  `dm_ns_2` varchar(254) default NULL,
  `dm_ns_3` varchar(254) default NULL,
  `dm_key` varchar(999) default NULL,
  `dm_authinfo` tinyint(1) default NULL,
  PRIMARY KEY  (`dm_id`)
);

DROP TABLE IF EXISTS `handle`;
CREATE TABLE `handle` (
  `hd_id` int(255) NOT NULL AUTO_INCREMENT,
  `hd_handle` varchar(32) default NULL,
  `hd_action` varchar(255) default NULL,
  `hd_type` varchar(255) default NULL,
  `hd_name` varchar(255) default NULL,
  `hd_organisation` varchar(255) default NULL,
  `hd_address` varchar(255) default NULL,
  `hd_pcode` varchar(20) default NULL,
  `hd_city` varchar(80) default NULL,
  `hd_country` varchar(2) default NULL,
  `hd_phone` varchar(255) default NULL,
  `hd_fax` varchar(255) default NULL,
  `hd_email` varchar(255) default NULL,
  `hd_sip` varchar(255) default NULL,
  `hd_disclose` varchar(255) default NULL,
  `hd_remarks_0` varchar(255) default NULL,
  `hd_remarks_1` varchar(255) default NULL,
  `hd_remarks_2` varchar(255) default NULL,
  `hd_remarks_3` varchar(255) default NULL,
  PRIMARY KEY  (`hd_id`)
);

